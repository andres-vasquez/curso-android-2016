package android.curso.serviciosweb3;

import android.widget.TextView;

public class EJemplo 
{
	TextView holamundo;
	String hola;
	int uno;
	boolean ok;
	
	public TextView getHolamundo() {
		return holamundo;
	}
	public void setHolamundo(TextView holamundo) {
		this.holamundo = holamundo;
	}
	public String getHola() {
		return hola;
	}
	public void setHola(String hola) {
		this.hola = hola;
	}
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public boolean isOk() {
		return ok;
	}
	public void setOk(boolean ok) {
		this.ok = ok;
	}
	
	
}
